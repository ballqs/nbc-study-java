package SecondJava;

public class Compare {
    public static void main(String[] args) {
        // 비교 연산자 예제

        // > : 크다
        System.out.println("10 > 9 결과 : " + (10 > 9));
        System.out.println("10 > 10 결과 : " + (10 > 10));
        System.out.println("10 > 11 결과 : " + (10 > 11));

        // >= : 같거나 크다
        System.out.println("10 >= 9 결과 : " + (10 >= 9));
        System.out.println("10 >= 10 결과 : " + (10 >= 10));
        System.out.println("10 >= 11 결과 : " + (10 >= 11));

        // < : 작다
        System.out.println("10 < 9 결과 : " + (10 < 9));
        System.out.println("10 < 10 결과 : " + (10 < 10));
        System.out.println("10 < 11 결과 : " + (10 < 11));


        // <= : 같거나 작다
        System.out.println("10 <= 9 결과 : " + (10 <= 9));
        System.out.println("10 <= 10 결과 : " + (10 <= 10));
        System.out.println("10 <= 11 결과 : " + (10 <= 11));

        // == : 같다
        System.out.println("10 == 9 결과 : " + (10 == 9));
        System.out.println("10 == 10 결과 : " + (10 == 10));

        // != : 다르다
        System.out.println("10 != 9 결과 : " + (10 != 9));
        System.out.println("10 != 10 결과 : " + (10 != 10));
    }
}
