public class Wrapper {
    public static void main(String[] args) {
        Integer num = 17;
        int num2 = num.intValue();
        System.out.println(num);
        System.out.println(num2);

        Character ch = 'X';
        char c = ch;
        System.out.println(ch);
        System.out.println(c);
    }
}
